package com.andrea.spesalo.Model;

public class CatalogoItem {

    private String Nome;
    private Boolean Offerta;

    public String getNome() {
        return Nome;
    }

    public void setNome(String nome) {
        Nome = nome;
    }

    public Boolean getOfferta() {
        return Offerta;
    }

    public void setOfferta(Boolean offerta) {
        Offerta = offerta;
    }
}
